#include <iostream>
#include <algorithm>
#include <vector>
#include <queue>
using namespace std;

#define INF 987654321

int N, M;
int graph[51][51];
bool visited[51][51];
int result = INF;

struct Location
{
	int y, x;
	int seq;
};

vector<Location> chickens;
vector<Location> home;
Location selected_chics[14];

int dx[4] = { 1,0,-1,0 };
int dy[4] = { 0,1,0,-1 };

bool isInside(int ny, int nx)
{
	return ny >= 0 && nx >= 0 && ny < N && nx < N;
}

// home위치도 구했고 정한 치킨집 selected_chics도 구했으면 bfs구할 필요가 없잖아! 수열로만 가능
void solve(int n, int m)
{
	int total_dist = 0;
	int dist = INF;
	// M개의 치킨집 빼고 다 폐점 시키기
	if (m == 0)
	{
		for (int i = 0; i < M; i++)
			graph[ selected_chics[i].y ][ selected_chics[i].x ] = 3;

		int select_size = sizeof(selected_chics) / sizeof(Location);

		// M개의 치킨집의 최단거리 총합 구하기
		for (int i = 0; i < home.size(); i++)
		{
			for (int j = 0; j < M; j++)
				dist = min(dist, abs(home[i].y - selected_chics[j].y) + abs(home[i].x - selected_chics[j].x));

			total_dist += dist;
			dist = INF;
		}

		if (result > total_dist)
			result = total_dist;

		total_dist = 0;
		for (int i = 0; i < M; i++)
			graph[selected_chics[i].y][selected_chics[i].x] = 2;

		return;
	}

	//M개의 치킨집 선정하기
	for (int i = 0; i < n; i++)
	{
		selected_chics[M - m] = chickens[i];
		if (M - m > 0 && selected_chics[M-m-1].seq > selected_chics[M-m].seq)
			continue;
		chickens.erase(chickens.begin() + i);
		//cout << selected_chics[M - m - 1].seq << "," << selected_chics[M - m].seq << endl;
		solve(n - 1, m - 1);
		chickens.insert(chickens.begin() + i, selected_chics[M - m]);
	}

}

int input()
{
	cin >> N >> M;

	int N_count = 2 * N;
	int chicken_count = 0;

	for (int i = 0; i < N; i++)
		for (int j = 0; j < N; j++)
		{
			cin >> graph[i][j];
			if (graph[i][j] == 1)
			{
				if (home.size() > N_count) 
					return 0;

				home.push_back({ i,j,0 });
			}
			if (graph[i][j] == 2)
			{
				++chicken_count;
				if (chicken_count > 13)
					return 0;
				
				chickens.push_back({ i,j,chicken_count });
			}
		}
	if (chicken_count < M) return 0;

	return 1;
}

int main()
{
	ios::sync_with_stdio(false);
	cin.tie(0);
	cout.tie(0);

	int impossible = input();

	// 전체 치킨집 개수, M크기
	solve( chickens.size(),M );

	if (impossible != 1)
	{
		cout << "wow" << endl;
		return INF;
	}
	cout << result;

	return 0;
}

